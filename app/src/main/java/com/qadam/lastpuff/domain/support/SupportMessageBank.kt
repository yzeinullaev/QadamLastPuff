    fun get(category: MessageCategory): List<String> =
        messages[category].orEmpty()

    fun dayMessage(hour: Int, session: MessageSession): String {
        val category = when (hour) {
            in 5..11 -> MessageCategory.MORNING
            in 18..23 -> MessageCategory.EVENING
            else -> MessageCategory.DAY
        }
        return session.pick(category)
    }

    fun memoryForReason(reason: String, goalTitle: String?): String? = when (reason) {
        "Дети" -> "Представь, как через год ты играешь с детьми без одышки."
        "Семья" -> "Твоя семья верит в тебе. Этот момент — для них тоже."
        "Спорт" -> "Ты уже ближе к своей первой лёгкой пробежке."
        "Деньги" -> goalTitle?.let { "Эта сигарета отдалит твою цель: $it." }
            ?: "Каждая сигарета — деньги, которые ты отдаёшь привычке."
        "Здоровье" -> "Твоё тело уже начало восстанавливаться. Не останавливай это."
        else -> null
    }
