    static func get(_ category: MessageCategory) -> [String] {
        messages[category] ?? []
    }

    static func dayMessage(hour: Int, session: MessageSession) -> String {
        let category: MessageCategory
        switch hour {
        case 5...11: category = .morning
        case 18...23: category = .evening
        default: category = .day
        }
        return session.pick(category)
    }

    static func checkInMessage(hour: Int, session: MessageSession) -> String {
        let intro = session.pick(.checkIn)
        let quote = dayMessage(hour: hour, session: session)
        return "\(intro) \(quote)"
    }

    static func holdingEncouragement(session: MessageSession) -> String {
        session.pick(.victory)
    }

    static func memoryForReason(_ reason: String, goalTitle: String?) -> String? {
        switch reason {
        case "Дети": return "Представь, как через год ты играешь с детьми без одышки."
        case "Семья": return "Твоя семья верит в тебе. Этот момент — для них тоже."
        case "Спорт": return "Ты уже ближе к своей первой лёгкой пробежке."
        case "Деньги":
            return goalTitle.map { "Эта сигарета отдалит твою цель: \($0)." }
                ?? "Каждая сигарета — деньги, которые ты отдаёшь привычке."
        case "Здоровье": return "Твоё тело уже начало восстанавливаться. Не останавливай это."
        default: return nil
        }
    }
